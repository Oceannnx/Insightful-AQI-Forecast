export const formatDate = (date: string) => {
    const dayNameTh = ['อาทิตย์', 'จันทร์', 'อังคาร', 'พุธ', 'พฤหัสบดี', 'ศุกร์', 'เสาร์']
    const monthNameTH = ['มกราคม', 'กุมภาพันธ์', 'มีนาคม', 'เมษายน', 'พฤษภาคม', 'มิถุนายน', 'กรกฎาคม', 'สิงหาคม', 'กันยายน', 'ตุลาคม', 'พฤศจิกายน', 'ธันวาคม']
    const dateObj = new Date(date)

    return `${dayNameTh[dateObj.getDay()]}, ${dateObj.getDate()} ${monthNameTH[dateObj.getMonth()]}`
}